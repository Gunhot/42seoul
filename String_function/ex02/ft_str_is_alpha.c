int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!('a' <= str[i] && str[i] <= 'z'))
			if (!('A' <= str[i] && str[i] <= 'Z'))
				return (0);
		i++;
	}
	return (1);
}
