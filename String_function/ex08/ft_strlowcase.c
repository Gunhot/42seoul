char	*ft_strlowcase(char *str)
{
	int	i;
	int	offset;

	offset = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			str[i] += offset;
		}
		i++;
	}
	return (str);
}
